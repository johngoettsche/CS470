#include "Factorial.h"

/**************************************************************************************************************/

int Multiply(int value1, int value2)
{
    return value1 * value2;
}

/**************************************************************************************************************/

int CalculateFactorial(int value)
{
    int result = 1;
    for (int i = 1; i <= value; ++i)
        result = Multiply(result, i);

    return result;
}

